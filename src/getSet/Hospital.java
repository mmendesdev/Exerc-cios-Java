package getSet;

public class Hospital {
    
      String nomePaciente;
      double cpd;
      int idade;
      Boolean estadoGrave;


   
    public String getNomePaciente() {
        return nomePaciente;
    }
    public void setNomePaciente(String nomePaciente) {
        this.nomePaciente = nomePaciente;
    }
    public double getCpd() {
        return cpd;
    }
    public void setCpd(double cpd) {
        this.cpd = cpd;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
    public Boolean getEstadoGrave() {
        return estadoGrave;
    }
    public void setEstadoGrave(Boolean estadoGrave) {
        this.estadoGrave = estadoGrave;
    }




}
